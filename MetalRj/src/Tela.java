    public Tela(JogoPrincipal p) {

        this.principal = p;
        this.coordY = 0;


        this.pp = new PersonagemP(this, 360, 360, 100, 40, 3, "nome", KeyEvent.VK_UP, KeyEvent.VK_DOWN, KeyEvent.VK_RIGHT, KeyEvent.VK_LEFT, KeyEvent.VK_SPACE, 9, 9);
        this.inimigo = new ArrayList<Inimigo>();
        this.timer = new Timer(100, this); //ms a classe é acionada
        this.setBackground(Color.white);
        this.aleatorio = new Random();
        this.imgcenarios = new Image[8];
        this.imgcenarios[0] = (new ImageIcon(this.getClass().getResource("/imagens/grama.jpeg"))).getImage();
        //1 é o fundo pintado de azul (mar)
        this.imgcenarios[4] = (new ImageIcon(this.getClass().getResource("/imagens/ponte.gif"))).getImage();
        this.imgcenarios[7] = (new ImageIcon(this.getClass().getResource("/imagens/vida_jpg.png"))).getImage();
        this.imgcenarios[5] = (new ImageIcon(this.getClass().getResource("/imagens/gameover.jpg"))).getImage();
        
        
        
        setIgnoreRepaint(true);
        setBackground(Color.DARK_GRAY); //fundo do cenário
    }
