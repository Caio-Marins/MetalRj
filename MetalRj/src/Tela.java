    public void iniciarJogo() {
        this.timer.start();
    }

    @Override
    public void paint(Graphics g) {
        super.paint(g);
        this.desenhar(g);
        pp.desenhar(g);
        for (Inimigo inimigo : this.inimigo) { //pega um objeto por vez do arrayList e chama de inimigo
            inimigo.desenhar(g);
        }
        g.dispose();
    }

    public void processarLogica() {
        this.pp.atualizaCoordenadas(principal.teclado);
        for (Inimigo inimigo : this.inimigo) {
            inimigo.atualizaCoordenadas();
        }
        this.pp.morrer();
        this.pontuacaoTela();
    }

    public void desenhar(Graphics g) {

        this.tempoDoCenario++;
        this.tempoAtingido--; // comeca em 3 e vai decrementando
        if (this.tempoAtingido == 0) { 
            this.pp.atingido = false;
        }
        if (this.tempoDoCenario == 5) { //a cada 5 * (tempo de atualização) uma linha desce
            this.tempoDoCenario = 0;
            atualizaCenario();
        }


        for (int i = 0; i < 10; i++) { // linhas 
            for (int j = 0; j < 22; j++) { // colunas
                g.drawImage(this.imgcenarios[this.cenario[i][j]], this.largura * j, this.largura * i, this.principal);
                pp.desenhar(g);
            }
        }
        if (this.pp.vidas == 0) {
            g.drawImage(this.imgcenarios[5], 0, 0, this.principal); //game over
            this.timer.stop();
        }
        if (this.pp.atingido) {
            g.drawImage(this.imgcenarios[6], 780, 300, this.principal); //cogumelo
        }

    }

    public void actionPerformed(ActionEvent e) {
        this.principal.teclado.poll(); // processa os eventos no teclado
        this.processarLogica(); // atualiza as coordenadas dos elementos
        this.repaint();
    }

    public void atualizaCenario() {
        for (int i = 9; i > 0; i--) { //10 linhas
            this.cenario[i] = this.cenario[i - 1]; //cada vez que atualiza o cenário, a linha que estava na posição i, fica na posição i-1
        }
        for (Inimigo inimigo : this.inimigo) {
            inimigo.posY = inimigo.posY + 40; //inimigo desce conforme cenário atualiza
            inimigo.posLinha++;
        }

        for (int i = 0; i < inimigo.size(); i++) { //inimigo.size = quantidade de inimigos dentro do arrayList
            Inimigo a = inimigo.get(i); //pegar uma cópia dentro do arrayList e chama de a
            if (a.posLinha >= 10) {
                inimigo.remove(i);
            }
        }
        this.cenario[0] = this.cenarioTodo[linhaCenarioTodo]; //linha atual do cenarioTodo vai para a linha 0 do cenario
        if (linhaCenarioTodo < 200) {
            linhaCenarioTodo++; //para vim a próxima linha do CenarioTodo
        } else {
            linhaCenarioTodo = 0; //se passar de 200 linhas, repete tudo de novo
        }
        if (linhaCenarioTodo % 5 == 0) { //200/5 resto 0 cria inimigo
            int tipo = aleatorio.nextInt(3);//para dizer que são duas imagem aleatórias
            for (int i = 5; i < 18; i++) { //nas primeiras e nas últimas 5 colunas sempre será grama
                if (this.validacao(0, i)) {
                    Inimigo novoInimigo = new Inimigo(this, i * 40, 0, 40, 0, i, tipo); //i*40 pq 40 é o tamanho da imagem
                    inimigo.add(novoInimigo);
                    i = 18; //para sair do for e nao criar um inimigo em cada coluna

                }
            }

        }

    }

    public boolean validacaoPonte(int coordX, int coordY) { //valida se posição tem uma ponte
        if (this.cenario[coordX][coordY] == 4) {
            return true;
        } else {
            return false;
        }
    }

    public boolean validacaoVida(int coordX, int coordY) { //valida se posição tem uma vida
        if (this.cenario[coordX][coordY] == 7) {
            return true;
        } else {
            return false;
        }
    }

    public boolean validacao(int coordX, int coordY) { //valida se posição do cenário = 0
        if (this.cenario[coordX][coordY] == 0) {
            return false;
        } else {
            return true;
        }
    }

    public boolean validacaoCombustivel(int coordX, int coordY) {
        if (this.cenario[coordX][coordY] == 3) { //para ver se na posição tem um posto
            return true;
        } else {
            return false;
        }
    }

    public void perdeVida() {
        if (pp.vidas > 0) {
            this.pp.vidas--; //decrementa vidas da nave
            this.principal.jLvidas.setText("" + this.pp.vidas);
            if (pp.vidas > 0) { //para quando tiver vidas = 0 não aparecer cogumelo
                this.pp.atingido = true;
                this.tempoAtingido = 3;
            }
        }

    }

 

    public void pontuacaoTela() {
        this.pp.pontuacao();
        this.principal.jLpontuacao.setText("" + this.pp.pontuacao);

    }
